                <p class="text" style="text-align: justify;"> <b>Bahan – Bahan Soup</b><br>
                  1.  6 potong paha ayam bagian bawah<br>
                  2.  1 bonggol samhong hidroponik<br>
                  3.  Daun dan bonggol akar ketumbar secukupnya<br>
                  4.  4-5 siung bawang putih geprek<br>
                  5.  1 ruas jahe geprek<br>
                  6.  2 sdm kecap asin<br>
                  7.  1/2 sdt merica bubuk<br>
                  8.  1 sdm garam<br>
                  9.  1/2 sdt gula pasir<br>
                  10. 1 liter air mineral<br><br>
                  <b>Cara Membuat</b><br>
              1. Cuci bersih paha ayam dan rebus paha ayam dengan 1 sdt garam selama 5 – 7 menit, angkat.<br><br>
              2. Cuci bersih sayuran dan bumbu, bilas dengan air mineral, geprek bawang putih, jahe dan akar ketumbar<br><br>
              3. Didihkan 1 liter air, masukkan bawang putih geprek, jahe geprek, batang dan akar ketumbar geprek, tambahkan garam, merica bubuk dan kecap asin, didihkan kembali<br><br>
              4. Setelah mendidih, masukkan ayam ,daun ketumbar, batang samhong dan gula pasir, masak hingga semua bahan matang. Sajikan chicken soup coriander dengan sambal kecap asin irisan cabe rawit dan samhong<br>

